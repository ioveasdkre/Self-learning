package _2_Raw_data_type;
public class _1_7_Cast
{
    // psvm + Tab 執行方法
    public static void main(String[] args)
    {
        float f = (float) 5.3;
        int i = (int)f;
        double k = 5.5;
        int s = (int)k;
        // sout + Tab 打印
        System.out.println(i);
        System.out.println(k);
        System.out.println(s);
    }
}